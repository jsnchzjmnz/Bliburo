    public void refrescarInfo(){
        if(ajustando){
            
            jToggleButtonAjustes.setText("Aplicar Cambios");
            
            jSpinnerDiasLibros.setValue(biblioteca.getAdministrador().getParametrizador().getDiasPrestamoLibro());
            jSpinnerDiasRevistas.setValue(biblioteca.getAdministrador().getParametrizador().getDiasPrestamoRevista());
            jSpinnerFecha.setValue(biblioteca.getAdministrador().getParametrizador().getFechaDelSistema());
            
            jLabelAjustesFecha.setVisible(false);
            jLabelDiasPrestamoLibro.setVisible(false);
            jLabelDiasPrestamoRevista.setVisible(false);
            jSpinnerDiasLibros.setVisible(true);
            jSpinnerDiasRevistas.setVisible(true);
            jSpinnerFecha.setVisible(true);
            
        }else{
            
            jToggleButtonAjustes.setText("Ajustar Parámetros");
            
            jLabelAjustesFecha.setText(biblioteca.getAdministrador().getParametrizador().getFechaDelSistema().toString());
            jLabelDiasPrestamoLibro.setText(Integer.toString(biblioteca.getAdministrador().getParametrizador().getDiasPrestamoLibro()));
            jLabelDiasPrestamoRevista.setText(Integer.toString(biblioteca.getAdministrador().getParametrizador().getDiasPrestamoRevista()));
            
            jLabelAjustesFecha.setVisible(true);
            jLabelDiasPrestamoLibro.setVisible(true);
            jLabelDiasPrestamoRevista.setVisible(true);
            jSpinnerDiasLibros.setVisible(false);
            jSpinnerDiasRevistas.setVisible(false);
            jSpinnerFecha.setVisible(false);
        }
        
    }
    
