    private void jToggleButtonAjustesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonAjustesActionPerformed
        if(ajustando){
            ajustando=false;
            
            biblioteca.getAdministrador().getParametrizador().setFechaDelSistema((Date) jSpinnerFecha.getValue());
            biblioteca.getAdministrador().getParametrizador().setDiasPrestamoLibro((int) jSpinnerDiasLibros.getValue());
            biblioteca.getAdministrador().getParametrizador().setDiasPrestamoRevista((int) jSpinnerDiasRevistas.getValue());
            
            refrescarInfo();
        }else{
            ajustando=true;
            refrescarInfo();
        }
        
    }//GEN-LAST:event_jToggleButtonAjustesActionPerformed

